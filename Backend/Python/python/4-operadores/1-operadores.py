# Operadores Aritmeticos

a = 10
b = 3

# Suma +
suma = a + b
print(f'Suma: {suma}')

# Resta -
resta = a - b
print(f'Resta: {resta}')

# Multiplicacion *
multiplicacion = a * b
print(f'Multiplicacion: {multiplicacion}')

# Division / (retorna un tipo float)
division = a / b
print(f'Division: {division:.2f}')

# Division entera (//)
division_entera = a // b
print(f'Division entera: {division_entera}')

# Modulo (%) residuo de la division
modulo = a % b
print(f'Residuo de la division: {modulo}')

# Exponente (potencia) **
exponente = a ** b # 10 elevado a la 3 = 10*10*10 = 1000
print(f'Exponente: {exponente}')