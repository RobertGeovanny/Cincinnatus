# Ejemplos de tipos de datos en Python

# Entero
edad = 30
print(f'Edad: {edad}')

# Numero con punto flotante (floot)
altura = 1.75
print(f'Altura: {altura}')

# Cadena de texto
nombre = 'Robert'
print(f'Nombre: {nombre}')

# Tipo boolean
es_estudiante = False
print(f'Es estudiante? {es_estudiante}')

# None, ausencia de valor
direccion = None
print(f'Dirreccion: {direccion}')