# Variables en Python

# Declaracion e Inicializacion de variables
edad = 28
altura = 1.79
pais = 'Mexico'

# Acceder a las variables
print(f'Valores iniciales: ')
print(f'Edad: {edad}')
print(f'Altura: {altura}')
print(f'Pais: {pais}')

# Modificar el valor de una variable
edad = 24
altura = 1.81

# Acceder a las variables
print('\nValores modificados: ')
print(f'Edad: {edad}')
print(f'Altura: {altura}')
print(f'Pais: {pais}')

# En Python el tipo es dinamico
edad = 'Treinta'
print(f'Edad: {edad}')